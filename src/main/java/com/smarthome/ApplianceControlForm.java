    private void toogleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_toogleButtonActionPerformed
        // TODO add your handling code here:
        String applianceId = applianceIdField.getText();
    boolean turnOn = toggleCheckBox.isSelected();

    new Thread(() -> {
        try {
            // Setup gRPC channel
            ManagedChannel channel = ManagedChannelBuilder
                    .forAddress("localhost", 9090)
                    .usePlaintext()
                    .build();

            // Create stub
            ApplianceControlServiceGrpc.ApplianceControlServiceBlockingStub stub =
                    ApplianceControlServiceGrpc.newBlockingStub(channel);

            // Build request
            ApplianceControlRequest request = ApplianceControlRequest.newBuilder()
                    .setApplianceId(applianceId)
                    .setTurnOn(turnOn)
                    .build();

            // Send request and get response
            ApplianceControlResponse response = stub.toggleAppliance(request);

            // Update UI on Swing thread
            javax.swing.SwingUtilities.invokeLater(() -> {
                responseTextArea.append("Response: " + response.getStatus() + "\n");
            });

            channel.shutdown();
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.SwingUtilities.invokeLater(() -> {
                responseTextArea.append("Error: " + e.getMessage() + "\n");
            });
        }
    }).start();

    }//GEN-LAST:event_toogleButtonActionPerformed

